public class Habitaciones {
    
    private int id;
    private int numH;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNumH() {
        return numH;
    }

    public void setNumH(int numH) {
        this.numH = numH;
    }

    @Override
    public String toString() {
        return "Habitaciones{" + "id=" + id + ", numH=" + numH + '}';
    }
    
    
    
    
}
