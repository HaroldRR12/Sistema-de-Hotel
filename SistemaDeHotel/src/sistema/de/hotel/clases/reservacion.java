package sistema.de.hotel.clases;

public class reservacion {
    private String id,usuario,idAbitacion,nombre;
    private int precio;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getIdAbitacion() {
        return idAbitacion;
    }

    public void setIdAbitacion(String idAbitacion) {
        this.idAbitacion = idAbitacion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }
    
    
    
}
